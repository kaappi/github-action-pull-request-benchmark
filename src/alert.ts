import * as core from '@actions/core';
import { Benchmark, BenchmarkResult } from './extract';
import { Config } from './config';
import { GitHubContext, getCurrentRepo, publishComment, getLatestWorkflowRunAttempt } from './git';

export type BenchmarkSuites = { [name: string]: Benchmark[] };
export interface DataJson {
    lastUpdate: number;
    repoUrl: string;
    entries: BenchmarkSuites;
}

export const SCRIPT_PREFIX = 'window.BENCHMARK_DATA = ';

interface Alert {
    current: BenchmarkResult;
    prev: BenchmarkResult;
    ratio: number;
}

function getRatio(current: BenchmarkResult, prev: BenchmarkResult): number {
    const divisor = current.biggerIsBetter ? current.value : prev.value;
    if (divisor === 0) return current.value === prev.value ? 1.0 : Infinity;
    return current.biggerIsBetter
        ? prev.value / current.value // e.g. current=100, prev=200
        : current.value / prev.value;
}

function findAlerts(curSuite: Benchmark, prevSuite: Benchmark, threshold: number): Alert[] {
    core.debug(`Comparing current:${curSuite.commit.id} and prev:${prevSuite.commit.id} for alert`);

    const alerts = [];
    for (const current of curSuite.benches.values()) {
        const prev = prevSuite.benches.get(current.name);
        if (prev === undefined) {
            core.debug(`Skipped because benchmark '${current.name}' is not found in previous benchmarks`);
            continue;
        }

        const ratio = getRatio(current, prev);

        if (!isFinite(ratio)) {
            core.debug(`Skipped '${current.name}': ratio is infinite (base value is zero)`);
            continue;
        }

        if (ratio > threshold) {
            alerts.push({ current, prev, ratio });
        }
    }

    return alerts;
}

function floatStr(n: number) {
    if (Number.isInteger(n)) {
        return n.toFixed(0);
    }

    if (n > 0.1) {
        return n.toFixed(2);
    }

    return n.toString();
}

function strVal(b: BenchmarkResult, omitUnit = false): string {
    let s = omitUnit ? `\`${b.value}\`` : `\`${b.value}\` ${b.unit}`;
    if (b.range) {
        s += ` (\`${b.range}\`)`;
    }
    return s;
}

function commonUnit(suite: Benchmark): string | null {
    let unit: string | null = null;
    for (const b of suite.benches.values()) {
        if (unit === null) unit = b.unit;
        else if (unit !== b.unit) return null;
    }
    return unit;
}

const BAR_FULL = '█';
const BAR_LIGHT = '░';
const BAR_WIDTH = 20;

function ratioBar(ratio: number): string {
    if (!isFinite(ratio) || ratio <= 0) return '';
    const clamped = Math.min(ratio, 2.0);
    const filled = Math.round((clamped / 2.0) * BAR_WIDTH);
    const empty = BAR_WIDTH - filled;
    return '`' + BAR_FULL.repeat(filled) + BAR_LIGHT.repeat(empty) + '`';
}

function ratioIndicator(ratio: number, biggerIsBetter: boolean): string {
    if (!isFinite(ratio)) return '⚪';
    const regression = biggerIsBetter ? ratio < 1.0 : ratio > 1.0;
    const improvement = biggerIsBetter ? ratio > 1.0 : ratio < 1.0;
    if (regression && ratio > 1.2) return '🔴';
    if (regression && ratio > 1.05) return '🟡';
    if (improvement && ratio < 0.95) return '🟢';
    return '⚪';
}

function commentFooter(gitHubContext: GitHubContext): string {
    const repo = getCurrentRepo(gitHubContext);
    // eslint-disable-next-line @typescript-eslint/camelcase
    const repoUrl = repo.html_url ?? '';
    const actionUrl = repoUrl + '/actions?query=workflow%3A' + encodeURIComponent(gitHubContext.workflow);

    return `This comment was automatically generated by [workflow](${actionUrl}).`;
}

function buildComment(
    benchName: string,
    curSuite: Benchmark,
    prevSuite: Benchmark,
    gitHubContext: GitHubContext,
): string {
    const curShort = curSuite.commit.id.slice(0, 7);
    const prevShort = prevSuite.commit.id.slice(0, 7);
    const unit = commonUnit(curSuite);
    const unitSuffix = unit ? ` (${unit})` : '';
    const omitUnit = unit !== null;
    const lines = [
        `# ${benchName}`,
        '',
        `| | Benchmark | Current (${curShort})${unitSuffix} | Base (${prevShort})${unitSuffix} | vs Base | Change |`,
        '|:-|:-|-:|-:|:-:|:-:|',
    ];

    for (const current of curSuite.benches.values()) {
        const prev = prevSuite.benches.get(current.name);

        if (prev) {
            const ratio = getRatio(current, prev);
            if (!isFinite(ratio)) {
                lines.push(`| 🆕 | \`${current.name}\` | ${strVal(current, omitUnit)} | ${strVal(prev, omitUnit)} | \`new\` | |`);
            } else {
                const indicator = ratioIndicator(ratio, current.biggerIsBetter);
                const bar = ratioBar(ratio);
                lines.push(`| ${indicator} | \`${current.name}\` | ${strVal(current, omitUnit)} | ${strVal(prev, omitUnit)} | \`${floatStr(ratio)}x\` | ${bar} |`);
            }
        } else {
            lines.push(`| 🆕 | \`${current.name}\` | ${strVal(current, omitUnit)} | — | | |`);
        }
    }

    lines.push(
        '',
        '> **vs Base** — ratio of current to base (`1.00x` = identical, lower = faster). **Change** — visual bar scaled 0–2x.',
        '> ⚪ no change · 🟢 faster · 🟡 slightly slower (>5%) · 🔴 regression (>20%)',
        '',
        commentFooter(gitHubContext),
    );

    return lines.join('\n');
}

function buildAlertComment(
    alerts: Alert[],
    benchName: string,
    curSuite: Benchmark,
    prevSuite: Benchmark,
    threshold: number,
    cc: string[],
    gitHubContext: GitHubContext,
): string {
    // Do not show benchmark name if it is the default value 'Benchmark'.
    const benchmarkText = benchName === 'Benchmark' ? '' : ` **'${benchName}'**`;
    const title = threshold === 0 ? '# Performance Report' : '# **Performance Alert**';
    const thresholdString = floatStr(threshold);
    const curShort = curSuite.commit.id.slice(0, 7);
    const prevShort = prevSuite.commit.id.slice(0, 7);
    const unit = commonUnit(curSuite);
    const unitSuffix = unit ? ` (${unit})` : '';
    const omitUnit = unit !== null;
    const lines = [
        title,
        '',
        `Possible performance regression was detected for benchmark${benchmarkText}.`,
        `Benchmark result of this commit is worse than the previous benchmark result exceeding threshold \`${thresholdString}\`.`,
        '',
        `| | Benchmark | Current (${curShort})${unitSuffix} | Base (${prevShort})${unitSuffix} | vs Base | Change |`,
        '|:-|:-|-:|-:|:-:|:-:|',
    ];

    for (const alert of alerts) {
        const { current, prev, ratio } = alert;
        const indicator = ratioIndicator(ratio, current.biggerIsBetter);
        const bar = ratioBar(ratio);
        lines.push(`| ${indicator} | \`${current.name}\` | ${strVal(current, omitUnit)} | ${strVal(prev, omitUnit)} | \`${floatStr(ratio)}x\` | ${bar} |`);
    }

    // Footer
    lines.push('', commentFooter(gitHubContext));

    if (cc.length > 0) {
        lines.push('', `CC: ${cc.join(' ')}`);
    }

    return lines.join('\n');
}

async function handleComment(
    benchName: string,
    curSuite: Benchmark,
    prevSuite: Benchmark,
    config: Config,
    gitHubContext: GitHubContext,
) {
    const { commentAlways, githubToken } = config;

    if (!commentAlways) {
        core.debug('Comment check was skipped because comment-always is disabled');
        return;
    }

    if (!githubToken) {
        throw new Error("'comment-always' input is set but 'github-token' input is not set");
    }

    core.debug('Commenting about benchmark comparison');

    const body = buildComment(benchName, curSuite, prevSuite, gitHubContext);

    await publishComment(curSuite.commit, body, githubToken, gitHubContext);
}

async function handleAlert(
    benchName: string,
    curSuite: Benchmark,
    prevSuite: Benchmark,
    config: Config,
    gitHubContext: GitHubContext,
) {
    const {
        alertThreshold,
        githubToken,
        commentOnAlert,
        failOnAlert,
        alertCommentCcUsers,
        failThreshold,
        fileToAnnotate,
    } = config;

    // annotations are not cleared when re-running a workflow, so we include the run attempt # in the annotation body
    const workflowRunAttempt = githubToken ? await getLatestWorkflowRunAttempt(githubToken, gitHubContext) : null;
    const generateWarningAnnotation = ({ prev, current, ratio }: Alert) => {
        core.warning(
            `Previous value was ${prev.value} and current value is ${current.value}.` +
                ` It is ${ratio}x worse, exceeding the warning threshold of ${floatStr(alertThreshold)}.`,
            {
                title: `${benchName} (attempt #${workflowRunAttempt}): potential performance regression detected for test '${current.name}'`,
                file: fileToAnnotate,
            },
        );
    };

    const generateErrorAnnotation = ({ prev, current, ratio }: Alert) => {
        core.error(
            `Previous value was ${prev.value} and current value is ${current.value}.` +
                ` It is ${ratio}x worse, exceeding the failure threshold of ${floatStr(failThreshold)}.`,
            {
                title: `${benchName} (attempt #${workflowRunAttempt}): performance regression detected for test '${current.name}'`,
                file: fileToAnnotate,
            },
        );
    };

    const alerts = findAlerts(curSuite, prevSuite, alertThreshold);
    if (alerts.length === 0) {
        core.debug('No performance alert found happily');
        return;
    }

    core.debug(`Found ${alerts.length} alerts`);
    const body = buildAlertComment(
        alerts,
        benchName,
        curSuite,
        prevSuite,
        alertThreshold,
        alertCommentCcUsers,
        gitHubContext,
    );
    let message = body;
    let url = null;

    if (commentOnAlert) {
        if (!githubToken) {
            throw new Error("'comment-on-alert' input is set but 'github-token' input is not set");
        }
        try {
            const res = await publishComment(curSuite.commit, body, githubToken, gitHubContext);
            // eslint-disable-next-line @typescript-eslint/camelcase
            url = res.data.html_url;
            message = body + `\nComment was generated at ${url}`;
        } catch (err) {
            console.error('Failed to publish performance alert comment:', err.message);
            console.log(message);
        }
    }

    const failures = alerts.filter(a => a.ratio > failThreshold);

    if (failThreshold !== alertThreshold) {
        const warnings = alerts.filter(a => a.ratio <= failThreshold);
        warnings.forEach(generateWarningAnnotation);
    } // else whether we error or warn on all alerts depends on the `fail on alert` flag

    if (failOnAlert) {
        failures.forEach(generateErrorAnnotation);

        // Note: alertThreshold is smaller than failThreshold. It was checked in config.ts
        const len = alerts.length;
        const threshold = floatStr(failThreshold);

        if (failures.length > 0) {
            core.debug('Mark this workflow as fail since one or more fatal alerts found');

            if (failThreshold !== alertThreshold) {
                // Prepend message that explains how these alerts were detected with different thresholds
                message = `${failures.length} of ${len} alerts exceeded the failure threshold \`${threshold}\` specified by fail-threshold input:\n\n${message}`;
            }
            throw new Error(message);
        } else {
            core.debug(
                `${len} alerts exceeding the alert threshold ${alertThreshold} were found but` +
                    ` all of them did not exceed the failure threshold ${threshold}`,
            );
        }
    } else {
        failures.forEach(generateWarningAnnotation);
    }
}

export async function compareBenchmarkAndAlert(
    bench: Benchmark,
    baseline: Benchmark,
    config: Config,
    gitHubContext: GitHubContext,
) {
    await handleComment(config.name, bench, baseline, config, gitHubContext);
    await handleAlert(config.name, bench, baseline, config, gitHubContext);
}
